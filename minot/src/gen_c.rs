    out.push_str(&format!("func create{root_go}Subscriber(topic string, queueSize int, qos Qos, callback func({root_type})) (uintptr, error) {{\n\th := RegisterHandler(callback)\n\tcTopic := C.CString(topic)\n\tdefer C.free(unsafe.Pointer(cTopic))\n\tC.registerGo{root_go}Callback(C.uintptr_t(h))\n\ts := C.mt_create_{root_safe}_subscriber(cTopic, C.uint32_t(queueSize), C.int(qos), C.uintptr_t(h))\n\tif s == 0 {{\n\t\treturn 0, fmt.Errorf(\"failed to create subscriber for topic %s\", topic)\n\t}}\n\treturn uintptr(s), nil\n}}\n\n"));
    out.push_str(&format!("var {root_go}Spec = MessageSpec[{root_type}]{{\n\tCreatePublisher: create{root_go}Publisher,\n\tPublish: publish{root_go},\n\tCreateSubscriber: create{root_go}Subscriber,\n}}\n\n"));
    out.push_str(&format!("func (n *Node) New{root_go}Publisher(topic string, qos Qos) (*Publisher[{root_type}], error) {{\n\treturn newPublisher(topic, qos, {root_go}Spec)\n}}\n\n"));
    out.push_str(&format!("func (n *Node) New{root_go}Subscriber(topic string, queueSize int, qos Qos, callback func({root_type})) (*Subscriber[{root_type}], error) {{\n\treturn newSubscriber(topic, queueSize, qos, {root_go}Spec, callback)\n}}\n"));
    out.push_str(&format!(
        "type {root_go}ServiceClient struct {{\n\thandle uintptr\n\ttopic  string\n}}\n\n"
    ));
    out.push_str(&format!(
        "type {root_go}ServiceServer struct {{\n\thandle uintptr\n\ttopic  string\n}}\n\n"
    ));
    out.push_str(&format!("func (n *Node) New{root_go}ServiceClient(topic string) (*{root_go}ServiceClient, error) {{\n\tcTopic := C.CString(topic)\n\tdefer C.free(unsafe.Pointer(cTopic))\n\th := C.mt_create_{root_safe}_service_client(cTopic)\n\tif h == 0 {{\n\t\treturn nil, fmt.Errorf(\"failed to create service client for topic %s\", topic)\n\t}}\n\treturn &{root_go}ServiceClient{{handle: uintptr(h), topic: topic}}, nil\n}}\n\n"));
    out.push_str(&format!("func (c *{root_go}ServiceClient) Request(req {root_type}, timeoutMs uint64) ({root_type}, error) {{\n\tcReq := toC{root_type}(req)\n\tdefer freeC{root_type}(&cReq)\n\tvar cResp *{root_c_type}\n\tres := C.mt_{root_safe}_service_request(C.uintptr_t(c.handle), (*{root_c_type})(unsafe.Pointer(&cReq)), C.uint64_t(timeoutMs), &cResp)\n\tif res != 0 {{\n\t\tvar zero {root_type}\n\t\treturn zero, fmt.Errorf(\"service request failed\")\n\t}}\n\tif cResp == nil {{\n\t\tvar zero {root_type}\n\t\treturn zero, fmt.Errorf(\"service returned nil response\")\n\t}}\n\tgoResp := cToGo{root_type}(*cResp)\n\tC.mt_free_{root_safe}_message(cResp)\n\treturn goResp, nil\n}}\n\n"));
    out.push_str(&format!("//export Go{root_go}ServiceCallback\nfunc Go{root_go}ServiceCallback(request *{root_c_type}, response *{root_c_type}, handle C.uintptr_t) C.int {{\n\tif request == nil || response == nil {{\n\t\treturn C.int(-1)\n\t}}\n\tcbAny, ok := GetHandler(uintptr(handle))\n\tif !ok {{\n\t\treturn C.int(-1)\n\t}}\n\tcb, ok := cbAny.(func({root_type}) ({root_type}, error))\n\tif !ok {{\n\t\treturn C.int(-1)\n\t}}\n\tresp, err := cb(cToGo{root_type}(*request))\n\tif err != nil {{\n\t\treturn C.int(-1)\n\t}}\n\t*response = toC{root_type}(resp)\n\treturn C.int(0)\n}}\n\n"));
    out.push_str(&format!("func (n *Node) New{root_go}ServiceServer(topic string, callback func({root_type}) ({root_type}, error)) (*{root_go}ServiceServer, error) {{\n\th := RegisterHandler(callback)\n\tcTopic := C.CString(topic)\n\tdefer C.free(unsafe.Pointer(cTopic))\n\tC.registerGo{root_go}ServiceCallback(C.uintptr_t(h))\n\ts := C.mt_create_{root_safe}_service_server(cTopic, C.uintptr_t(h))\n\tif s == 0 {{\n\t\treturn nil, fmt.Errorf(\"failed to create service server for topic %s\", topic)\n\t}}\n\treturn &{root_go}ServiceServer{{handle: uintptr(s), topic: topic}}, nil\n}}\n\n"));
    out.push_str(&format!("type {root_go}GoalState int\n\nconst (\n\t{root_go}GoalStateInvalidGoalID {root_go}GoalState = 0\n\t{root_go}GoalStateAccepted {root_go}GoalState = 1\n\t{root_go}GoalStateExecuting {root_go}GoalState = 2\n\t{root_go}GoalStateCanceling {root_go}GoalState = 3\n\t{root_go}GoalStateSucceeded {root_go}GoalState = 4\n\t{root_go}GoalStateAborted {root_go}GoalState = 5\n\t{root_go}GoalStateCanceled {root_go}GoalState = 6\n)\n\n"));
