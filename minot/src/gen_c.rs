fn collect_existing_go_types(dir: &Path, exclude_file: Option<&str>) -> Result<BTreeSet<String>> {
    let mut out = BTreeSet::new();
    if !dir.exists() {
        return Ok(out);
    }
    for entry in fs::read_dir(dir)? {
        let entry = entry?;
        let path = entry.path();
        if !path.is_file() {
            continue;
        }
        let Some(name) = path.file_name().and_then(|n| n.to_str()) else {
            continue;
        };
        if !name.starts_with("msg_") || !name.ends_with(".go") {
            continue;
        }
        if exclude_file.is_some_and(|e| e == name) {
            continue;
        }
        let contents = fs::read_to_string(&path)?;
        for line in contents.lines() {
            let trimmed = line.trim();
            if !trimmed.starts_with("type ") || !trimmed.ends_with(" struct {") {
                continue;
            }
            let rest = &trimmed["type ".len()..trimmed.len() - " struct {".len()];
            if !rest.is_empty() && rest.chars().next().unwrap_or('a').is_ascii_uppercase() {
                out.insert(rest.to_string());
            }
        }
    }
    Ok(out)
}

