fn rust_from_expr(src: &str, ty: &TypeRef, names: &BTreeMap<MsgRef, String>) -> Result<String> {
    Ok(match ty {
        TypeRef::Primitive(_) => src.to_string(),
        TypeRef::String => format!(
            "{{ let c = CString::new({src}.replace('\\0', \" \" )).unwrap_or_else(|_| CString::new(\"\").unwrap()); let ptr = c.into_raw(); minot_c_string {{ ptr: ptr as *const c_char, len: {src}.len() }} }}"
        ),
        TypeRef::Struct(s) => {
            let t = names.get(s).ok_or_else(|| anyhow!("missing type"))?;
            format!("{}_from_rust(&{})", t.to_snake_case(), src)
        }
        TypeRef::Vec(inner) => {
            let elem_ty = rust_c_type_of(inner, names)?;
            let slice_ty = rust_slice_type_name(inner, names)?;
            let inner_expr = rust_from_expr("*item", inner, names)?;
            format!(
                "{{ let mut tmp: Vec<{elem_ty}> = Vec::with_capacity({src}.len()); for item in {src}.iter() {{ tmp.push({inner_expr}); }} let len = tmp.len(); let ptr = if len == 0 {{ std::ptr::null() }} else {{ let boxed = tmp.into_boxed_slice(); Box::into_raw(boxed) as *const {elem_ty} }}; {slice_ty} {{ ptr, len }} }}"
            )
        }
        TypeRef::Array(inner, len) => {
            let elem_ty = rust_c_type_of(inner, names)?;
            let inner_expr = rust_from_expr("*item", inner, names)?;
            format!(
                "{{ let tmp: Vec<{elem_ty}> = {src}.iter().map(|item| {inner_expr}).collect(); tmp.try_into().unwrap_or_else(|_| panic!(\"array size mismatch for {}\")) }}",
                len
            )
        }
    })
}

fn rust_to_expr(src: &str, ty: &TypeRef, names: &BTreeMap<MsgRef, String>) -> Result<String> {
    Ok(match ty {
        TypeRef::Primitive(_) => src.to_string(),
        TypeRef::String => format!("c_string_to_string({src})?"),
        TypeRef::Struct(s) => {
            let t = names.get(s).ok_or_else(|| anyhow!("missing type"))?;
            format!("{}_to_rust(&{src} as *const _)?", t.to_snake_case())
        }
        TypeRef::Vec(inner) => {
            let elem_ty = rust_c_type_of(inner, names)?;
            let inner_expr = rust_to_expr("*item", inner, names)?;
            format!(
                "{{ let slice: &[{elem_ty}] = if {src}.ptr.is_null() {{ &[] }} else {{ unsafe {{ std::slice::from_raw_parts({src}.ptr, {src}.len) }} }}; let mut out = Vec::with_capacity(slice.len()); for item in slice.iter() {{ out.push({inner_expr}); }} out }}"
            )
        }
        TypeRef::Array(inner, len) => {
            let rust_ty = rust_rust_type(inner)?;
            let inner_expr = rust_to_expr("item", inner, names)?;
            format!(
                "{{ let mut tmp: Vec<{rust_ty}> = Vec::with_capacity({len}); for item in {src}.iter().cloned() {{ tmp.push({inner_expr}); }} tmp.try_into().map_err(|_| ())? }}"
            )
        }
    })
}

fn rust_free_stmt(
    expr: &str,
    ty: &TypeRef,
    names: &BTreeMap<MsgRef, String>,
    indent: &str,
) -> Result<String> {
    Ok(match ty {
        TypeRef::Primitive(_) => String::new(),
        TypeRef::String => format!(
            "{indent}if !{expr}.ptr.is_null() {{ drop(CString::from_raw({expr}.ptr as *mut c_char)); }}\n"
        ),
        TypeRef::Struct(s) => {
            let t = names.get(s).ok_or_else(|| anyhow!("missing type"))?;
            format!(
                "{indent}{}_free_in_place(&mut {expr});\n",
                t.to_snake_case()
            )
        }
        TypeRef::Vec(inner) => {
            let elem_ty = rust_c_type_of(inner, names)?;
            let mut out = format!(
                "{indent}if !{expr}.ptr.is_null() {{\n{indent}    let mut vec: Vec<{elem_ty}> = Vec::from_raw_parts({expr}.ptr as *mut {elem_ty}, {expr}.len, {expr}.len);\n{indent}    for item in vec.iter_mut() {{\n"
            );
            out.push_str(&rust_free_stmt(
                "*item",
                inner,
                names,
                &format!("{indent}        "),
            )?);
            out.push_str(&format!("{indent}    }}\n{indent}}}\n"));
            out
        }
        TypeRef::Array(inner, len) => {
            let inner_stmt = rust_free_stmt(
                &format!("{expr}[i]"),
                inner,
                names,
                &format!("{indent}    "),
            )?;
            if inner_stmt.trim().is_empty() {
                return Ok(String::new());
            }
            let mut out = format!("{indent}for i in 0..{} {{\n", len);
            out.push_str(&inner_stmt);
            out.push_str(&format!("{indent}}}\n"));
            out
        }
    })
}

