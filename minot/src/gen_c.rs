fn ffi_lib_rs() -> String {
    r#"use std::collections::VecDeque;
use std::ffi::{c_char, CStr, CString};
use std::sync::{Arc, LazyLock, Mutex, OnceLock};

use mt_pubsub::{Node, NodeConfig, Qos};
pub mod generated;

static RT: LazyLock<Mutex<Option<Arc<tokio::runtime::Runtime>>>> =
    LazyLock::new(|| Mutex::new(None));
static NODE: LazyLock<Mutex<Option<Arc<Node>>>> = LazyLock::new(|| Mutex::new(None));

static LOG_MAX_LEVEL: LazyLock<Mutex<log::LevelFilter>> =
    LazyLock::new(|| Mutex::new(log::LevelFilter::Info));
static LOG_QUEUE: LazyLock<Mutex<VecDeque<LogRecordEntry>>> =
    LazyLock::new(|| Mutex::new(VecDeque::new()));
static LOGGER_INIT: OnceLock<()> = OnceLock::new();

#[derive(Clone)]
struct LogRecordEntry {
    level: i32,
    target: String,
    message: String,
}

struct FfiLogger;

impl log::Log for FfiLogger {
    fn enabled(&self, metadata: &log::Metadata<'_>) -> bool {
        metadata.level() <= *LOG_MAX_LEVEL.lock().unwrap()
    }

    fn log(&self, record: &log::Record<'_>) {
        if !self.enabled(record.metadata()) {
            return;
        }
        let target = record.target();
        if is_zenoh_target(target) && record.level() < log::Level::Error {
            return;
        }
        let level = match record.level() {
            log::Level::Trace => 0,
            log::Level::Debug => 1,
            log::Level::Info => 2,
            log::Level::Warn => 3,
            log::Level::Error => 4,
        };
        LOG_QUEUE.lock().unwrap().push_back(LogRecordEntry {
            level,
            target: target.to_owned(),
            message: format!("{}", record.args()),
        });
    }

    fn flush(&self) {}
}

fn is_zenoh_target(target: &str) -> bool {
    target.starts_with("zenoh")
        || target.starts_with("zenoh_")
        || target.starts_with("zenoh::")
        || target.starts_with("zenoh_transport")
        || target.starts_with("zenoh_link")
        || target.starts_with("zenoh_protocol")
}

fn parse_level(filter: &str) -> Option<log::LevelFilter> {
    let first = filter.split(',').next()?.trim().to_ascii_lowercase();
    match first.as_str() {
        "trace" => Some(log::LevelFilter::Trace),
        "debug" => Some(log::LevelFilter::Debug),
        "info" => Some(log::LevelFilter::Info),
        "warn" | "warning" => Some(log::LevelFilter::Warn),
        "error" => Some(log::LevelFilter::Error),
        "off" => Some(log::LevelFilter::Off),
        _ => None,
    }
}

pub(crate) fn qos_from_i32(qos: i32) -> Option<Qos> {
    match qos {
        0 => Some(Qos::Reliable),
        1 => Some(Qos::BestEffort),
        _ => None,
    }
}

#[no_mangle]
pub unsafe extern "C" fn mt_node_init(name: *const c_char, node_qos: i32) -> i32 {
    if name.is_null() {
        return -1;
    }
    let Some(mode) = qos_from_i32(node_qos) else {
        return -1;
    };
    let name_str = match CStr::from_ptr(name).to_str() {
        Ok(s) => s,
        Err(_) => return -1,
    };

    let mut srt = RT.lock().unwrap();
    if srt.is_none() {
        srt.replace(Arc::new(
            tokio::runtime::Builder::new_multi_thread()
                .enable_all()
                .build()
                .unwrap(),
        ));
    }
    let rt = srt.as_ref().unwrap().clone();
    drop(srt);

    let node = rt.block_on(async { Node::create(NodeConfig::new(name_str).mode(mode)).await });
    match node {
        Ok(n) => {
            NODE.lock().unwrap().replace(Arc::new(n));
            0
        }
        Err(_) => -1,
    }
}

#[no_mangle]
pub extern "C" fn mt_sleep_ms(ms: u64) {
    std::thread::sleep(std::time::Duration::from_millis(ms));
}

#[no_mangle]
pub extern "C" fn mt_node_shutdown_cancelled() -> i32 {
    let node_lock = NODE.lock().unwrap();
    let Some(node) = node_lock.as_ref() else {
        return 1;
    };
    if node.shutdown_token().is_cancelled() {
        1
    } else {
        0
    }
}

#[no_mangle]
pub unsafe extern "C" fn mt_configure_logging(filter: *const c_char) -> i32 {
    let filter_str = if filter.is_null() {
        "info".to_owned()
    } else {
        match CStr::from_ptr(filter).to_str() {
            Ok(s) if !s.trim().is_empty() => s.trim().to_owned(),
            Ok(_) => "info".to_owned(),
            Err(_) => return -3,
        }
    };
    let Some(level) = parse_level(&filter_str) else {
        return -3;
    };
    {
        let mut max_level = LOG_MAX_LEVEL.lock().unwrap();
        *max_level = level;
    }
    let _ = LOGGER_INIT.get_or_init(|| {
        let _ = log::set_boxed_logger(Box::new(FfiLogger));
    });
    log::set_max_level(level);
    0
}

#[no_mangle]
pub unsafe extern "C" fn mt_take_log_record(
    out_level: *mut i32,
    out_target: *mut *mut c_char,
    out_message: *mut *mut c_char,
) -> i32 {
    if out_level.is_null() || out_target.is_null() || out_message.is_null() {
        return -1;
    }
    let mut q = LOG_QUEUE.lock().unwrap();
    let Some(entry) = q.pop_front() else {
        return 0;
    };
    let Ok(c_target) = CString::new(entry.target.replace('\0', " ")) else { return -2; };
    let Ok(c_message) = CString::new(entry.message.replace('\0', " ")) else { return -2; };
    *out_level = entry.level;
    *out_target = c_target.into_raw();
    *out_message = c_message.into_raw();
    1
}

#[no_mangle]
pub unsafe extern "C" fn mt_free_c_string(ptr: *mut c_char) {
    if ptr.is_null() {
        return;
    }
    drop(CString::from_raw(ptr));
}
"#
    .to_string()
}

