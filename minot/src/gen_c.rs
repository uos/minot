#[derive(Debug, Clone, PartialEq, Eq, PartialOrd, Ord)]
struct MsgRef {
    pkg: String,
    name: String,
}

#[derive(Debug, Clone)]
struct StructSpec {
    fields: Vec<FieldSpec>,
}

#[derive(Debug, Clone)]
struct FieldSpec {
    name: String,
    ty: TypeRef,
}

#[derive(Debug, Clone)]
enum TypeRef {
    Primitive(&'static str),
    String,
    Vec(Box<TypeRef>),
    Array(Box<TypeRef>, usize),
    Struct(MsgRef),
}

struct Resolver {
    files: HashMap<String, File>,
    structs: BTreeMap<MsgRef, StructSpec>,
    can_fetch: bool,
}

impl Resolver {
    fn new(root_pkg: String, root_file: File, can_fetch: bool) -> Self {
        let mut files = HashMap::new();
        files.insert(root_pkg.clone(), root_file);
        Self {
            files,
            structs: BTreeMap::new(),
            can_fetch,
        }
    }

    fn resolve_message(&mut self, msg: &MsgRef) -> Result<()> {
        if self.structs.contains_key(msg) {
            return Ok(());
        }
        self.ensure_file_loaded(&msg.pkg)?;
        let file = self
            .files
            .get(&msg.pkg)
            .ok_or_else(|| anyhow!("Missing parsed file for package {}", msg.pkg))?
            .clone();

        let st = if let Some(st) = find_struct(&file, &msg.name) {
            st
        } else if self.can_fetch {
            let source = find_or_download_rust_source(&msg.pkg, &msg.name.to_snake_case())?;
            let parsed = syn::parse_file(&source)?;
            ensure_supported_constructs(&parsed)?;
            if let Some(st) = find_struct(&parsed, &msg.name) {
                let entry = self
                    .files
                    .entry(msg.pkg.clone())
                    .or_insert_with(|| syn::parse_file("").unwrap());
                entry.items.extend(parsed.items.clone());
                st
            } else {
                bail!(
                    "Could not find struct '{}' in package '{}'.",
                    msg.name,
                    msg.pkg
                );
            }
        } else {
            bail!(
                "Could not find struct '{}' in package '{}'.",
                msg.name,
                msg.pkg
            );
        };

        let mut fields = Vec::new();
        for field in st.fields {
            let field_ident = field
                .ident
                .ok_or_else(|| anyhow!("Unsupported tuple field in {}", msg.name))?;
            let ty = self.parse_type(&field.ty, &msg.pkg)?;
            fields.push(FieldSpec {
                name: field_ident.to_string(),
                ty,
            });
        }

        self.structs.insert(msg.clone(), StructSpec { fields });

        let nested: Vec<MsgRef> = self
            .structs
            .get(msg)
            .unwrap()
            .fields
            .iter()
            .flat_map(|f| collect_struct_refs(&f.ty))
            .collect();

        for nested_msg in nested {
            self.resolve_message(&nested_msg)?;
        }

        Ok(())
    }

    fn ensure_file_loaded(&mut self, pkg: &str) -> Result<()> {
        if self.files.contains_key(pkg) {
            return Ok(());
        }
        if !self.can_fetch {
            bail!(
                "Unsupported external type reference to package '{}' when using piped stdin source. Include referenced structs in stdin source.",
                pkg
            );
        }
        let source = find_or_download_rust_source(pkg, "mod")
            .or_else(|_| find_or_download_package_module(pkg))?;
        let file = syn::parse_file(&source)?;
        ensure_supported_constructs(&file)?;
        self.files.insert(pkg.to_string(), file);
        Ok(())
    }

    fn parse_type(&self, ty: &Type, current_pkg: &str) -> Result<TypeRef> {
        if let Some(inner) = is_vec(ty) {
            return Ok(TypeRef::Vec(Box::new(self.parse_type(inner, current_pkg)?)));
        }
        if let Some((inner, len)) = is_array(ty) {
            return Ok(TypeRef::Array(
                Box::new(self.parse_type(inner, current_pkg)?),
                len,
            ));
        }

        match ty {
            Type::Path(TypePath { path, .. }) => {
                let segs: Vec<String> = path.segments.iter().map(|s| s.ident.to_string()).collect();
                if segs.is_empty() {
                    bail!("Unsupported empty type path");
                }
                let last = segs.last().unwrap().as_str();
                if let Some(p) = primitive(last) {
                    return Ok(TypeRef::Primitive(p));
                }
                if last == "String" {
                    return Ok(TypeRef::String);
                }
                if segs.len() == 1 {
                    return Ok(TypeRef::Struct(MsgRef {
                        pkg: current_pkg.to_string(),
                        name: segs[0].clone(),
                    }));
                }
                if segs.len() >= 4 && segs[0] == "crate" && segs[2] == "msg" {
                    return Ok(TypeRef::Struct(MsgRef {
                        pkg: segs[1].clone(),
                        name: segs[3].clone(),
                    }));
                }
                bail!("Unsupported type path '{}'.", segs.join("::"));
            }
            _ => bail!("Unsupported Rust type construct: {:?}", ty),
        }
    }
}

fn find_struct(file: &File, name: &str) -> Option<syn::ItemStruct> {
    for item in &file.items {
        if let Item::Struct(st) = item {
            if st.ident == name {
                return Some(st.clone());
            }
        }
    }
    None
}

fn collect_struct_refs(ty: &TypeRef) -> Vec<MsgRef> {
    match ty {
        TypeRef::Struct(s) => vec![s.clone()],
        TypeRef::Vec(inner) | TypeRef::Array(inner, _) => collect_struct_refs(inner),
        _ => vec![],
    }
}

fn collect_ordered_types(
    resolver: &Resolver,
    root: &MsgRef,
    visited: &mut BTreeSet<MsgRef>,
    ordered: &mut Vec<MsgRef>,
) -> Result<()> {
    if visited.contains(root) {
        return Ok(());
    }
    visited.insert(root.clone());
    let s = resolver
        .structs
        .get(root)
        .ok_or_else(|| anyhow!("Missing struct {}", root.name))?;
    for field in &s.fields {
        for nested in collect_struct_refs(&field.ty) {
            collect_ordered_types(resolver, &nested, visited, ordered)?;
        }
    }
    ordered.push(root.clone());
    Ok(())
}

fn go_type_of(ty: &TypeRef, names: &BTreeMap<MsgRef, String>) -> Result<String> {
    Ok(match ty {
        TypeRef::Primitive(p) => p.to_string(),
        TypeRef::String => "string".to_string(),
        TypeRef::Vec(inner) => format!("[]{}", go_type_of(inner, names)?),
        TypeRef::Array(inner, len) => format!("[{}]{}", len, go_type_of(inner, names)?),
        TypeRef::Struct(msg) => names
            .get(msg)
            .cloned()
            .ok_or_else(|| anyhow!("Internal error: missing type mapping for {}", msg.name))?,
    })
}

fn choose_unique_type_name(msg: &MsgRef, root: &MsgRef, used: &BTreeSet<String>) -> String {
    if msg == root {
        return go_root_type_name(root);
    }
    let base = format!("{}{}", msg.pkg.to_pascal_case(), msg.name.to_pascal_case());
    if !used.contains(&base) {
        return base;
    }
    let prefixed = format!("{}{}", msg.pkg.to_pascal_case(), base);
    if !used.contains(&prefixed) {
        return prefixed;
    }
    let mut i = 2usize;
    loop {
        let candidate = format!("{prefixed}{i}");
        if !used.contains(&candidate) {
            return candidate;
        }
        i += 1;
    }
}

fn go_root_type_name(root: &MsgRef) -> String {
    format!(
        "{}{}",
        root.pkg.to_pascal_case(),
        root.name.to_pascal_case()
    )
}

fn ensure_supported_constructs(file: &File) -> Result<()> {
    for item in &file.items {
        if let Item::Enum(en) = item {
            bail!(
                "Unsupported Rust construct enum '{}'. Only struct-based messages are supported.",
                en.ident
            );
        }
    }
    Ok(())
}

fn primitive(name: &str) -> Option<&'static str> {
    match name {
        "f64" => Some("float64"),
        "f32" => Some("float32"),
        "u8" => Some("uint8"),
        "i8" => Some("int8"),
        "u16" => Some("uint16"),
        "i16" => Some("int16"),
        "u32" => Some("uint32"),
        "i32" => Some("int32"),
        "u64" => Some("uint64"),
        "i64" => Some("int64"),
        "bool" => Some("bool"),
        _ => None,
    }
}

fn is_vec(ty: &Type) -> Option<&Type> {
    match ty {
        Type::Path(TypePath { path, .. }) => {
            let last = path.segments.last()?.ident.to_string();
            if last == "Vec" {
                if let syn::PathArguments::AngleBracketed(args) = &path.segments.last()?.arguments {
                    if let Some(syn::GenericArgument::Type(inner_ty)) = args.args.first() {
                        return Some(inner_ty);
                    }
                }
            }
            None
        }
        _ => None,
    }
}

fn is_array(ty: &Type) -> Option<(&Type, usize)> {
    if let Type::Array(arr) = ty {
        let len = match &arr.len {
            Expr::Lit(expr_lit) => {
                if let syn::Lit::Int(lit_int) = &expr_lit.lit {
                    lit_int.base10_parse::<usize>().ok()?
                } else {
                    return None;
                }
            }
            _ => return None,
        };
        Some((&arr.elem, len))
    } else {
        None
    }
}

fn find_or_download_package_module(pkg: &str) -> Result<String> {
    let output = Command::new("cargo")
        .args(["metadata", "--format-version", "1"])
        .output()?;
    let stdout = String::from_utf8_lossy(&output.stdout);
    if let Some(code) = extract_pkg_mod_from_metadata_str(&stdout, pkg) {
        return Ok(code);
    }

    let downloaded_stdout = get_or_create_downloaded_metadata_stdout()?;
    extract_pkg_mod_from_metadata_str(&downloaded_stdout, pkg)
        .ok_or_else(|| anyhow!("Could not find source module for package '{}'.", pkg))
}

fn extract_pkg_mod_from_metadata_str(stdout: &str, pkg: &str) -> Option<String> {
    let json = serde_json::from_str::<serde_json::Value>(stdout).ok()?;
    let packages = json.get("packages")?.as_array()?;
    for package in packages {
        if package.get("name").and_then(|n| n.as_str()) == Some("ros2-interfaces-jazzy-rkyv") {
            let manifest_path = package.get("manifest_path")?.as_str()?;
            let mut path = PathBuf::from(manifest_path);
            path.pop();
            path.push("src");
            path.push(pkg);
            path.push("msg");
            path.push("mod.rs");
            if path.exists() {
                return fs::read_to_string(path).ok();
            }
        }
    }
    None
}

fn find_or_download_rust_source(pkg: &str, msg: &str) -> Result<String> {
    if let Some(code) = extract_source_from_metadata(pkg, msg) {
        return Ok(code);
    }

    let downloaded_stdout = get_or_create_downloaded_metadata_stdout()?;
    extract_source_from_metadata_str(&downloaded_stdout, pkg, msg)
        .ok_or_else(|| anyhow!("Could not find message definition for {}/{}.", pkg, msg))
}

fn get_or_create_downloaded_metadata_stdout() -> Result<String> {
    {
        let cache = DOWNLOADED_METADATA_STDOUT.lock().unwrap();
        if let Some(stdout) = cache.as_ref() {
            return Ok(stdout.clone());
        }
    }

    eprintln!("ros2-interfaces-jazzy-rkyv not found locally. Downloading crate...");
    let temp_dir = std::env::temp_dir().join(format!("minot_dl_{}", std::process::id()));
    let _ = fs::remove_dir_all(&temp_dir);
    fs::create_dir_all(&temp_dir)?;

    let cargo_toml = "[package]\nname = \"minot_dummy\"\nversion = \"0.1.0\"\nedition = \"2021\"\n[dependencies]\nros2-interfaces-jazzy-rkyv = \"*\"\n";
    fs::write(temp_dir.join("Cargo.toml"), cargo_toml)?;
    fs::create_dir_all(temp_dir.join("src"))?;
    fs::write(temp_dir.join("src/lib.rs"), "")?;

    let status = Command::new("cargo")
        .args(["generate-lockfile"])
        .current_dir(&temp_dir)
        .status()?;

    if !status.success() {
        let _ = fs::remove_dir_all(&temp_dir);
        bail!("Failed to download ros2-interfaces crate.");
    }

    let output = Command::new("cargo")
        .args(["metadata", "--format-version", "1"])
        .current_dir(&temp_dir)
        .output()?;

    let stdout = String::from_utf8_lossy(&output.stdout);
    let stdout_owned = stdout.to_string();
    let _ = fs::remove_dir_all(&temp_dir);

    let mut cache = DOWNLOADED_METADATA_STDOUT.lock().unwrap();
    *cache = Some(stdout_owned.clone());
    Ok(stdout_owned)
}

fn extract_source_from_metadata(pkg: &str, msg: &str) -> Option<String> {
    let output = Command::new("cargo")
        .args(["metadata", "--format-version", "1"])
        .output()
        .ok()?;
    let stdout = String::from_utf8_lossy(&output.stdout);
    extract_source_from_metadata_str(&stdout, pkg, msg)
}

fn extract_source_from_metadata_str(stdout: &str, pkg: &str, msg: &str) -> Option<String> {
    let json = serde_json::from_str::<serde_json::Value>(stdout).ok()?;
    let packages = json.get("packages")?.as_array()?;
    for package in packages {
        if package.get("name").and_then(|n| n.as_str()) == Some("ros2-interfaces-jazzy-rkyv") {
            let manifest_path = package.get("manifest_path")?.as_str()?;
            let mut path = PathBuf::from(manifest_path);
            path.pop();
            path.push("src");
            path.push(pkg);
            path.push("msg");
            path.push(format!("{}.rs", msg));
            if path.exists() {
                return fs::read_to_string(path).ok();
            }
        }
    }
    None
}

pub struct GeneratedOutput {
    pub c_header: String,
    pub go_wrapper: String,
    pub rs_ffi: String,
}

pub fn parse_and_generate(
    input_rust_code: &str,
    target_type: &str,
    full_type_path: &str,
) -> Result<GeneratedOutput> {
    let file = syn::parse_file(input_rust_code)?;
    ensure_supported_constructs(&file)?;

    let mut c = String::from("#include <stdint.h>\n#include <stdbool.h>\n#include <stddef.h>\n\n");
    let mut go = String::from("package minot\n\n");
    let rs = format!("// Placeholder rust ffi for {}\n", full_type_path);

    for item in file.items {
        if let Item::Struct(st) = item {
            let name = st.ident.to_string();
            c.push_str("typedef struct {\n");
            go.push_str(&format!("type {} struct {{\n", name));
            for field in st.fields {
                let f = field.ident.unwrap().to_string();
                if let Some((inner, len)) = is_array(&field.ty) {
                    let (ct, gt) = c_and_go_scalar(inner)?;
                    c.push_str(&format!("    {} {}[{}];\n", ct, f, len));
                    go.push_str(&format!("\t{} [{}]{}\n", f.to_pascal_case(), len, gt));
                } else if let Some(inner) = is_vec(&field.ty) {
                    let (_ct, gt) = c_and_go_scalar(inner)?;
                    c.push_str(&format!("    void* {};\n", f));
                    go.push_str(&format!("\t{} []{}\n", f.to_pascal_case(), gt));
                } else {
                    let (ct, gt) = c_and_go_scalar(&field.ty)?;
                    c.push_str(&format!("    {} {};\n", ct, f));
                    go.push_str(&format!("\t{} {}\n", f.to_pascal_case(), gt));
                }
            }
            c.push_str(&format!("}} minot_{};\n\n", name));
            go.push_str("}\n\n");
            if name == target_type {
                c.push_str(&format!(
                    "void mt_subscribe_{}(const char* topic, uintptr_t handle);\nvoid mt_publish_{}(const char* topic, const minot_{}* message);\n",
                    name, name, name
                ));
            }
        }
    }

    Ok(GeneratedOutput {
        c_header: c,
        go_wrapper: go,
        rs_ffi: rs,
    })
}

fn c_and_go_scalar(ty: &Type) -> Result<(&'static str, &'static str)> {
    if let Type::Path(TypePath { path, .. }) = ty {
        let last = path.segments.last().unwrap().ident.to_string();
        return Ok(match last.as_str() {
            "f64" => ("double", "float64"),
            "f32" => ("float", "float32"),
            "u8" => ("uint8_t", "uint8"),
            "i8" => ("int8_t", "int8"),
            "u16" => ("uint16_t", "uint16"),
            "i16" => ("int16_t", "int16"),
            "u32" => ("uint32_t", "uint32"),
            "i32" => ("int32_t", "int32"),
            "u64" => ("uint64_t", "uint64"),
            "i64" => ("int64_t", "int64"),
            "bool" => ("bool", "bool"),
            "String" => ("const char*", "string"),
            _ => ("void*", "any"),
        });
    }
    bail!("Unsupported scalar type in parse_and_generate")
}

