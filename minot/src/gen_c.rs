pub fn gen_c_command(type_name: String) -> Result<(), Box<dyn std::error::Error>> {
    let mut input_rust_code = String::new();
    if !std::io::stdin().is_terminal() {
        std::io::stdin().read_to_string(&mut input_rust_code)?;
    }

    if input_rust_code.is_empty() {
        let parts: Vec<&str> = type_name.split('/').collect();
        if parts.len() == 2 {
            input_rust_code = find_or_download_rust_source(parts[0], &parts[1].to_snake_case())
                .unwrap_or_default();
        }
    }

    if input_rust_code.is_empty() {
        return Err(anyhow!(
            "Could not find rust source for type: {}. Pipe the rust struct definition via stdin.",
            type_name
        )
        .into());
    }

    let parts: Vec<&str> = type_name.split('/').collect();
    let target_struct_name = if parts.len() == 2 {
        parts[1]
    } else {
        &type_name
    };
    let generated = parse_and_generate(&input_rust_code, target_struct_name, &type_name)?;

    println!("// Generated by Minot for type: {}", type_name);
    println!("{}", generated.c_header);
    println!("\n/* GO WRAPPER */\n{}", generated.go_wrapper);
    println!("\n/* RUST FFI */\n{}", generated.rs_ffi);
    Ok(())
}

pub fn init_go_command(path: PathBuf) -> Result<(), Box<dyn std::error::Error>> {
    let abs_path = path.canonicalize().unwrap_or(path.clone());
    let project_name = abs_path
        .file_name()
        .ok_or_else(|| anyhow!("Could not determine project name from path"))?
        .to_str()
        .ok_or_else(|| anyhow!("Invalid UTF-8 in project path"))?;

    println!(
        "Initializing Go project '{}' at {:?}...",
        project_name, path
    );

    fs::create_dir_all(path.join("pkg/minot"))?;
    fs::create_dir_all(path.join("examples"))?;
    fs::create_dir_all(path.join("ffi/src/generated"))?;

    fs::write(
        path.join("go.mod"),
        format!(
            "module {}\n\ngo 1.21\n\nrequire github.com/charmbracelet/log v0.4.0\n",
            project_name
        ),
    )?;
    fs::write(path.join(".gitignore"), generated_gitignore())?;

    fs::write(path.join("pkg/minot/minot.go"), go_core_file())?;
    let repo_root = resolve_minot_repo_root()?;
    fs::write(path.join("ffi/Cargo.toml"), ffi_cargo_toml(&repo_root))?;
    fs::write(path.join("ffi/src/lib.rs"), ffi_lib_rs())?;
    fs::write(path.join("ffi/src/generated/mod.rs"), "")?;
    add_go_msg_command(vec!["std_msgs/String".to_string()], path.clone())?;
    generate_examples(&path, project_name)?;

    println!(
        "Done. String bindings and examples generated. Next run: minot add-go-msg <pkg/Msg> --project {}",
        path.display()
    );
    Ok(())
}

pub fn add_go_msg_command(
    type_names: Vec<String>,
    project: PathBuf,
) -> Result<(), Box<dyn std::error::Error>> {
    if type_names.is_empty() {
        return Err(anyhow!("Provide at least one type name like package/Message").into());
    }

    let project_minot_dir = project.join("pkg/minot");
    if !project_minot_dir.exists() {
        return Err(anyhow!(
            "{}/pkg/minot does not exist. Run `minot init-go <dir>` first.",
            project.display()
        )
        .into());
    }

    let mut stdin_source = String::new();
    if !std::io::stdin().is_terminal() {
        std::io::stdin().read_to_string(&mut stdin_source)?;
    }

    let use_stdin = !stdin_source.trim().is_empty();

    let mut existing_types = collect_existing_go_types(&project_minot_dir, None)?;
    let ffi_generated_dir = project.join("ffi/src/generated");
    fs::create_dir_all(&ffi_generated_dir)?;
    let mut generated_modules = collect_existing_generated_modules(&ffi_generated_dir)?;
    for type_name in &type_names {
        let parts: Vec<&str> = type_name.split('/').collect();
        if parts.len() != 2 {
            return Err(anyhow!(
                "Type name '{}' must be in format 'package/Message'",
                type_name
            )
            .into());
        }
        let pkg = parts[0].to_string();
        let msg_name = parts[1].to_string();

        let source = if use_stdin {
            stdin_source.clone()
        } else {
            find_or_download_rust_source(&pkg, &msg_name.to_snake_case())?
        };

        let source_file = syn::parse_file(&source)?;
        ensure_supported_constructs(&source_file)?;

        let mut resolver = Resolver::new(pkg.clone(), source_file, !use_stdin);
        let root = MsgRef {
            pkg: pkg.clone(),
            name: msg_name.clone(),
        };
        resolver.resolve_message(&root)?;

        let module_name = format!("msg_{}_{}", pkg.to_snake_case(), msg_name.to_snake_case());
        let out_name = format!("{module_name}.go");
        let generated_go = go_typed_message_spec(&resolver, &root, &existing_types)?;
        let generated_rs = rust_typed_message_module(&resolver, &root, &existing_types)?;
        fs::write(project_minot_dir.join(out_name), &generated_go)?;
        fs::write(
            ffi_generated_dir.join(format!("{module_name}.rs")),
            generated_rs,
        )?;
        generated_modules.insert(module_name);

        let new_types = collect_types_from_generated_go(&generated_go);
        for t in new_types {
            existing_types.insert(t);
        }

        println!(
            "Added message {}/{} as type {} to {}/pkg/minot.",
            pkg,
            msg_name,
            go_root_type_name(&root),
            project.display()
        );
    }

    if !type_names.iter().any(|t| t == "std_msgs/String") {
        let default_type = "std_msgs/String";
        let parts: Vec<&str> = default_type.split('/').collect();
        let pkg = parts[0].to_string();
        let msg_name = parts[1].to_string();
        let source = find_or_download_rust_source(&pkg, &msg_name.to_snake_case())?;
        let source_file = syn::parse_file(&source)?;
        ensure_supported_constructs(&source_file)?;
        let mut resolver = Resolver::new(pkg.clone(), source_file, true);
        let root = MsgRef {
            pkg: pkg.clone(),
            name: msg_name.clone(),
        };
        resolver.resolve_message(&root)?;
        let out_name = format!(
            "msg_{}_{}.go",
            pkg.to_snake_case(),
            msg_name.to_snake_case()
        );
        if !project_minot_dir.join(&out_name).exists() {
            let generated_go = go_typed_message_spec(&resolver, &root, &existing_types)?;
            let generated_rs = rust_typed_message_module(&resolver, &root, &existing_types)?;
            fs::write(project_minot_dir.join(out_name), &generated_go)?;
            let module_name = format!("msg_{}_{}", pkg.to_snake_case(), msg_name.to_snake_case());
            fs::write(
                ffi_generated_dir.join(format!("{module_name}.rs")),
                generated_rs,
            )?;
            generated_modules.insert(module_name);
            for t in collect_types_from_generated_go(&generated_go) {
                existing_types.insert(t);
            }
            println!(
                "Added default message std_msgs/String to {}/pkg/minot.",
                project.display()
            );
        }
    }

    write_generated_modules_mod_rs(&ffi_generated_dir, &generated_modules)?;
    build_generated_ffi(&project)?;
    Ok(())
}

fn resolve_minot_repo_root() -> Result<PathBuf> {
    let cwd = std::env::current_dir()?;
    if cwd.join("mt_pubsub").exists() && cwd.join("mt_sea").exists() {
        return Ok(cwd);
    }
    if let Ok(root) = std::env::var("MINOT_REPO_ROOT") {
        let root = PathBuf::from(root);
        if root.join("mt_pubsub").exists() && root.join("mt_sea").exists() {
            return Ok(root);
        }
    }
    let manifest_parent = PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .map(PathBuf::from);
    if let Some(root) = manifest_parent {
        if root.join("mt_pubsub").exists() && root.join("mt_sea").exists() {
            return Ok(root);
        }
    }
    bail!(
        "Could not find minot repo root with mt_pubsub and mt_sea. Run from repo root or set MINOT_REPO_ROOT."
    );
}

fn build_generated_ffi(project: &Path) -> Result<()> {
    let ffi_dir = project.join("ffi");
    println!("Compiling generated FFI glue code (cargo build --release)...");
    let status = Command::new("cargo")
        .args(["build", "--release"])
        .current_dir(&ffi_dir)
        .stdin(Stdio::null())
        .stdout(Stdio::inherit())
        .stderr(Stdio::inherit())
        .status()?;
    std::io::stdout().flush()?;

    if !status.success() {
        bail!(
            "Failed to build generated ffi crate at {}",
            ffi_dir.display()
        );
    }
    Ok(())
}

