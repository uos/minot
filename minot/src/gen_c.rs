fn go_c_field_type(ty: &TypeRef, names: &BTreeMap<MsgRef, String>) -> Result<String> {
    Ok(match ty {
        TypeRef::Primitive(p) => go_primitive_c_type(p)?.to_string(),
        TypeRef::String => "minot_c_string".to_string(),
        TypeRef::Vec(inner) => go_slice_type_name(inner, names)?,
        TypeRef::Array(_, _) => bail!("Array C type requires field declaration context"),
        TypeRef::Struct(s) => format!(
            "minot_{}",
            names
                .get(s)
                .ok_or_else(|| anyhow!("missing type"))?
                .to_snake_case()
        ),
    })
}

fn go_c_field_decl(name: &str, ty: &TypeRef, names: &BTreeMap<MsgRef, String>) -> Result<String> {
    Ok(match ty {
        TypeRef::Array(inner, len) => {
            format!("    {} {}[{}];\n", go_c_type_of(inner, names)?, name, len)
        }
        _ => format!("    {} {};\n", go_c_field_type(ty, names)?, name),
    })
}

fn go_c_type_of(ty: &TypeRef, names: &BTreeMap<MsgRef, String>) -> Result<String> {
    Ok(match ty {
        TypeRef::Primitive(p) => go_primitive_c_type(p)?.to_string(),
        TypeRef::String => "minot_c_string".to_string(),
        TypeRef::Struct(s) => format!(
            "minot_{}",
            names
                .get(s)
                .ok_or_else(|| anyhow!("missing type"))?
                .to_snake_case()
        ),
        TypeRef::Array(inner, len) => format!("[{}]{}", len, go_c_type_of(inner, names)?),
        TypeRef::Vec(_) => bail!("Unsupported nested vector element in C type mapping"),
    })
}

fn go_slice_type_name(inner: &TypeRef, names: &BTreeMap<MsgRef, String>) -> Result<String> {
    Ok(format!("minot_c_slice_{}", go_elem_suffix(inner, names)?))
}

fn go_elem_suffix(inner: &TypeRef, names: &BTreeMap<MsgRef, String>) -> Result<String> {
    Ok(match inner {
        TypeRef::Primitive(p) => p.to_string(),
        TypeRef::String => "string".to_string(),
        TypeRef::Struct(s) => names
            .get(s)
            .ok_or_else(|| anyhow!("missing type"))?
            .to_snake_case(),
        _ => bail!("Unsupported vector element type"),
    })
}

